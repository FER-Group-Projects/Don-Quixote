package hr.fer.zemris.projekt.gui.view.sprite;

public class Frame {

    public final double x;
    public final double y;
    public final double width;
    public final double height;

    public Frame(double x, double y, double width, double height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

}
